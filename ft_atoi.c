int	ft_atoi(char *str)
{
	int	i;
	int	a;
	int	c;
	int	b;

	b = 0;
	a = 0;
	i = 0;
	while ((str[i] > 8 && str[i] < 14) || str[i] == 32)
			i++;
	if (str[i] == 43 || str[i] == 45)
	{
		i++;
		c = 1;
	}
	while (str [i] >= 48 && str[i] <= 57)
	{
		b = b * 10 - (str[i] - 48);
		i++;
	}
	if (c == 0)
		b = -b;
	return (b);
}
